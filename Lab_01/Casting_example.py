# Python Casting

# INT 
x = int(1)   # x will be 1
y = int(225.8) # y will be 2
z = int("35") # z will be 3


# Float 
x = float(12)     # x will be 1.0
y = float(2.85)   # y will be 2.8
z = float("30")   # z will be 3.0
w = float("42.01") # w will be 4.2

# String 
x = str("HELLO WROLD") # x will be 's1'
y = str(2025)    # y will be '2'
z = str(35.0)  # z will be '3.0'