# Python Variables - Assign Multiple Values

x, y, z = "Orange", "Banana", "Watermelon"
print(x)
print(y)
print(z)

# One Value to Multiple Variables
x = y = z = "Bananaaa"
print(x)
print(y)
print(z)

# Unpack a Collection
fruits = ["Pineapple", "banana", "Watermelon"]
x, y, z = fruits
print(x)
print(y)
print(z)